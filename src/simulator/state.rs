use std::default::Default;

use isa::Word;

///////////////////////////////////////////////////////////////////////////////
//// TYPES

pub type RegisterFile = [Word; 33];

///////////////////////////////////////////////////////////////////////////////
//// STRUCTS

#[derive(Copy, Clone)]
pub struct State {
    pub register: RegisterFile,
}

///////////////////////////////////////////////////////////////////////////////
//// IMPLEMENTATIONS

impl Default for State {
    fn default() -> State {
        State {
            register: [0 as Word; 33],
        }
    }
}

